var menu = [];
jQuery('.online_startup_businessswiper-slide').each( function(index){
    menu.push( jQuery(this).find('.online_startup_businessslide-inner').attr("data-text") );
});
var interleaveOffset = 0.5;
var swiperOptions = {
    loop: true,
    speed: 1000,
    parallax: true,
    autoplay: {
        delay: 6500,
        disableOnInteraction: false,
    },
    watchSlidesProgress: true,
    pagination: {
        el: '.online_startup_businessswiper-pagination',
        clickable: true,
    },
    navigation: {
        nextEl: '.online_startup_businessswiper-button-next',
        prevEl: '.online_startup_businessswiper-button-prev',
    },
    on: {
        progress: function() {
            var swiper = this;
            for (var i = 0; i < swiper.slides.length; i++) {
                var slideProgress = swiper.slides[i].progress;
                var innerOffset = swiper.width * interleaveOffset;
                var innerTranslate = slideProgress * innerOffset;
                swiper.slides[i].querySelector(".online_startup_businessslide-inner").style.transform =
                "translate3d(" + innerTranslate + "px, 0, 0)";
            }      
        },

        touchStart: function() {
          var swiper = this;
          for (var i = 0; i < swiper.slides.length; i++) {
            swiper.slides[i].style.transition = "";
          }
        },

        setTransition: function(speed) {
            var swiper = this;
            for (var i = 0; i < swiper.slides.length; i++) {
                swiper.slides[i].style.transition = speed + "ms";
                swiper.slides[i].querySelector(".online_startup_businessslide-inner").style.transition =
                speed + "ms";
            }
        }
    }
};
var swiper = new Swiper(".online_startup_businessswiper-container", swiperOptions);
